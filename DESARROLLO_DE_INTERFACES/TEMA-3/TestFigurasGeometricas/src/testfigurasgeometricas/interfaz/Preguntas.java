    private void jButtonEnviarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonEnviarActionPerformed
        
        float puntuacion = calcularPuntuacion();
        int aciertos = contarAciertos();
                
        pantallaPrincipal.establecerPuntuacion(puntuacion);
        pantallaPrincipal.establecerAciertos(aciertos);
        
        // Preguntar al usuario si está seguro de sus respuestas
        int respuesta = JOptionPane.showConfirmDialog(this, 
            "¿Está seguro de sus respuestas?", 
            "Confirmar", 
            JOptionPane.YES_NO_OPTION);

        // Verificar la respuesta del usuario
        if (respuesta == JOptionPane.YES_OPTION) {
            dispose();
            pantallaPrincipal.setVisible(true);
        }
        
    }//GEN-LAST:event_jButtonEnviarActionPerformed

