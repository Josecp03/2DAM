    private float calcularPuntuacion() {
        
        // Inicializar la variable
        float puntuacion = 0;

        // Comprobar la puntuación de la primera pregunta
        if (jRadioButton1B.isSelected()) {
            puntuacion += 2.5;
        }

        // Comprobar la puntuación de la segunda pregunta
        if (jRadioButton2C.isSelected()) {
            puntuacion += 2.5;
        }

        // Comprobar la puntuación de la tercera pregunta
        if (jRadioButton3D.isSelected()) {
            puntuacion += 2.5;
        }
        
        // Comprobar la puntuación de la cuarta pregunta
        if (jRadioButton4C.isSelected()) {
            puntuacion += 2.5;
        }
        
        // Devolver la puntuación que hay conseguido el usuario
        return puntuacion;
        
    }

