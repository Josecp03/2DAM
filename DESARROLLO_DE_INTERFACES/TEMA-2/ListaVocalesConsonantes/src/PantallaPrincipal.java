    private void jButtonEnterActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonEnterActionPerformed
        
        // Establecer una variable con la palabra ingresada en el JTetxField
        String word = jTextFieldWord.getText();
        
        // Comprobar que sea una palabra
        if (isWord(word)) {
                        
            // Comprobar si empieza por vocal o no
            if (startVowel(word)) {
                
                // Añadimos la palabra al array correspondiente
                arrayVowels.add(word);
                
                // Mostrar los elementos del array
                modelo1.removeAllElements();
                for (int i = 0; i < arrayVowels.size(); i++) {
                    modelo1.addElement(arrayVowels.get(i));
                }
                
            } else {
                
                // Añadimos la palabra al array correspondiente
                arrayConsonants.add(word);
                
                // Mostrar los elementos del array
                modelo2.removeAllElements();
                for (int i = 0; i < arrayConsonants.size(); i++) {
                    modelo2.addElement(arrayConsonants.get(i));
                }
                
            }
            
        }
        
    }//GEN-LAST:event_jButtonEnterActionPerformed

