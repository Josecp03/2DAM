    // Método para comprobar que sea una palabra y no un número
    private static boolean isWord(String input) {
        return input.matches("[a-zA-Z]+");
    }
    
    // Método para comprobar si la palabra empieza por vocal
    private static boolean startVowel(String word) {
    
        // Pasar la palabra a mayúsculas para realizar bien la comparación
        word = word.toUpperCase();
        
        // Coger el primer caracter de la palabra
        char firstChar = word.charAt(0);
        
        // Comprobar que sea vocal
        if (firstChar == 'A' || firstChar == 'E' || firstChar == 'I' || firstChar == 'O' || firstChar == 'U') {
            return true;
        } else {
            return false;
        }
       
    }
    
 
