    private void jButtonLimpiarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonLimpiarActionPerformed

        int resultado = JOptionPane.showConfirmDialog(this,
                "¿Seguro que quieres borrar los campos?",
                "Confirmación",
                JOptionPane.YES_NO_OPTION);

        if (resultado == JOptionPane.YES_NO_OPTION) {
            // Limpiar los datos
            jTextFieldDNI.setText("");
            jTextFieldCorreo.setText("");
            jTextFieldDireccion.setText("");
            jTextFieldEmpresa.setText("");
            jTextFieldTelefono.setText("");
        }


    }//GEN-LAST:event_jButtonLimpiarActionPerformed

    private void jButtonNuevoProveedorActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonNuevoProveedorActionPerformed

        // Comprobar que los campos no estén vacíos
        if (camposVacios()) {

            // Mostrar mensaje de error
            JOptionPane.showMessageDialog(this,
                    "Error: Los campos no pueden estar vacíos",
                    "Error de entrada",
                    JOptionPane.ERROR_MESSAGE);

        } else {

            // Comprobar que la dirección de correo sea válida
            if (verificarDireccionCorreo(jTextFieldCorreo.getText().toString())) {

                // Comprobar que el DNI tiene la longitud adecuada
                if (jTextFieldDNI.getText().toString().length() == 9) {

                    // Comprobar que en el campo de la empresa solo se hayan introducido letras
                    if (soloLetras(jTextFieldEmpresa.getText().toString())) {

                        // Comrobar que en el campo del teléfono solo se hayan introducido números
                        if (soloNumeros(jTextFieldTelefono.getText().toString())) {

                            // Comprobar que en el campo del teléfono se haya introducido un teléfono válido
                            if (jTextFieldTelefono.getText().toString().length() == 9) {

                                // Asignar los valores introducidos por el usuario a variables
                                String dni = jTextFieldDNI.getText().toString();
                                String empresa = jTextFieldEmpresa.getText().toString();
                                String direccion = jTextFieldDireccion.getText().toString();
                                int telefono = Integer.parseInt(jTextFieldTelefono.getText().toString());
                                String pais = jComboBoxPaises.getSelectedItem().toString();
                                String correo = jTextFieldCorreo.getText().toString();

                                // Crear objeto de tipo Proveedor
                                Proveedor p = new Proveedor(dni, empresa, direccion, telefono, pais, correo);

                                // Cerrar ventana actual    
                                dispose();

                                // Crear la nueva tabla
                                tabla = new Tabla();

                                // Pasarle la pelicula a la tabla
                                tabla.añadirProveedor(p);

                                // Actualizra la tabla con el proveedor
                                tabla.actualizarTabla();

                                // Hacer visible la ventana con la tabla
                                tabla.setVisible(true);

                                // Cerrar ventana actual
                                dispose();

                            } else {

                                // Mostrar mensaje de error
                                JOptionPane.showMessageDialog(this,
                                        "Error: El teléfono debe se der de 9 dígitos.",
                                        "Error de entrada",
                                        JOptionPane.ERROR_MESSAGE);

                            }

                        } else {

                            // Mostrar mensaje de error
                            JOptionPane.showMessageDialog(this,
                                    "Error: No se pueden introducir letras en el campo del teléfono.",
                                    "Error de entrada",
                                    JOptionPane.ERROR_MESSAGE);

                        }

                    } else {

                        // Mostrar mensaje de error
                        JOptionPane.showMessageDialog(this,
                                "Error: No se pueden introducir números en el campo de la empresa.",
                                "Error de entrada",
                                JOptionPane.ERROR_MESSAGE);

                    }

                } else {

                    // Mostrar mensaje de error
                    JOptionPane.showMessageDialog(this,
                            "Error: El DNI debe de contener 9 caracteres.",
                            "Error de entrada",
                            JOptionPane.ERROR_MESSAGE);

                }

            } else {

                // Mostrar mensaje de error
                JOptionPane.showMessageDialog(this,
                        "Error: Dirección de correo no válida.",
                        "Error de entrada",
                        JOptionPane.ERROR_MESSAGE);

            }

        }


    }//GEN-LAST:event_jButtonNuevoProveedorActionPerformed

    public boolean soloLetras(String cadena) {
        return cadena.matches("^[a-zA-ZáéíóúÁÉÍÓÚüÜñÑ ]+$");
    }

    public boolean soloNumeros(String cadena) {
        return cadena.matches("^[0-9]+$");
    }

    private void cargarComboBoxConImagenes() {

        DefaultComboBoxModel<String> model = new DefaultComboBoxModel<>();
        model.addElement("España");
        model.addElement("Bélgica");
        model.addElement("Brasil");
        model.addElement("USA");
        jComboBoxPaises.setModel(model);
        jComboBoxPaises.setRenderer(new ListCellRenderer<String>() {

            @Override
            public Component getListCellRendererComponent(JList<? extends String> list, String value, int index, boolean isSelected, boolean cellHasFocus) {
                JLabel label = new JLabel();
                switch (value) {

                    case "España":
                        label.setIcon(new ImageIcon(getClass().getResource("/imgs/espana.png")));
                        label.setText("España");
                        break;

                    case "Bélgica":
                        label.setIcon(new ImageIcon(getClass().getResource("/imgs/belgica.png")));
                        label.setText("Bélgica");
                        break;

                    case "Brasil":
                        label.setIcon(new ImageIcon(getClass().getResource("/imgs/brasil.png")));
                        label.setText("Brasil");
                        break;

                    case "USA":
                        label.setIcon(new ImageIcon(getClass().getResource("/imgs/estados-unidos.png")));
                        label.setText("USA");
                        break;

                }

                if (isSelected) {
                    label.setBackground(list.getSelectionBackground());
                    label.setOpaque(true);
                }

                return label;

            }

        });

    }


