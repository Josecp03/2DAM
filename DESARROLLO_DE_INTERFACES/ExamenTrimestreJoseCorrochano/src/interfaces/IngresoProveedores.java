    private boolean camposVacios() {
        return jTextFieldCorreo.getText().toString().isEmpty()
                || jTextFieldDNI.getText().toString().isEmpty()
                || jTextFieldDireccion.getText().toString().isEmpty()
                || jTextFieldEmpresa.getText().toString().isEmpty()
                || jTextFieldTelefono.getText().toString().isEmpty();
    }

    // Método que verifica mediante una expresión regular si es una dirección de correo válida
    public boolean verificarDireccionCorreo(String direccionCorreo) {
        return direccionCorreo.matches("^[^\\s@]+@[^\\s@]+\\.[^\\s@]+$");
    }

