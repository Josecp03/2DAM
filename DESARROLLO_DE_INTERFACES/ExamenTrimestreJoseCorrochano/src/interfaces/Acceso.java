    private void jButtonOjoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonOjoActionPerformed

        // Comprobar si el carácter de eco es el predeterminado (es decir, la contraseña está oculta)
        if (jPasswordFieldContraseña.getEchoChar() == (char) 0) {

            // Si el campo está visible, lo ocultamos
            jPasswordFieldContraseña.setEchoChar('*');

        } else {

            // Si el campo está oculto, lo mostramos
            jPasswordFieldContraseña.setEchoChar((char) 0);

        }
    }//GEN-LAST:event_jButtonOjoActionPerformed

    private void jButtonEntrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonEntrarActionPerformed

        if (camposVacios()) {

            // Mostrar mensaje de error
            JOptionPane.showMessageDialog(this,
                "Error: Los cmapos no pueden estar vacíos.",
                "Error de entrada",
                JOptionPane.ERROR_MESSAGE);

        } else {

            if (soloLetras(jTextFieldUsuario.getText().toString())) {

                // Cerrar ventana actual
                dispose();

                // Crear siguiente ventana
                Seleccion pantallaPrincipal = new Seleccion();

                // Hacer visible la siguiente pantalla
                pantallaPrincipal.setVisible(true);

            } else {

                // Mostrar mensaje de error
                JOptionPane.showMessageDialog(this,
                    "Error: No se pueden introducir letras en el campo de usuario.",
                    "Error de entrada",
                    JOptionPane.ERROR_MESSAGE);

            }

        }
    }//GEN-LAST:event_jButtonEntrarActionPerformed

