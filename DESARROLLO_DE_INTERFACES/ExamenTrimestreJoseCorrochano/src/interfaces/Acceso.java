    public boolean soloLetras(String cadena) {
        return cadena.matches("^[a-zA-ZáéíóúÁÉÍÓÚüÜñÑ ]+$");
    }

    public boolean camposVacios() {
        return jTextFieldUsuario.getText().toString().isEmpty() || jPasswordFieldContraseña.getPassword().length == 0;
    }


